#<<---Indexing and Slicing--->>#
text = "I'm programming in Python"

#-> Indexing
print(text[19]) # Prints a specific position of the String.

#-> Slicing
print(text[10:19]) # Prints a range of the String.

#<--Additional Trick-->#
print(f"Character sequence -> {text[::4]}") # (Every four characters).
# Prints with jump of characters.
