#<<---Indexing y Slicing--->>#
text = "Estoy programando en Python"

#-> Indexing
print(text[19]) #Dar una posicion especifica en el String

#-> Slicing
print(text[10:19]) #Imprimir en un rango del String

#<--Truco adicional-->#
print(f"Secuencia de caracteres -> {text[::4]}") #(Cada cuatro caracteres)
