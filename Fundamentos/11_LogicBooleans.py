#<<---Logica de los booleanos--->>#

#<<---AND--->>#
and1 = True and True #-> True
and2 = True and False #-> False
and3 = False and True #-> False
and4 = False and False #-> False

#<<---OR--->>#
or1 = True or True #-> True
or2 = True or False #-> True
or3 = False or True #-> True
or4 = False or False #-> False