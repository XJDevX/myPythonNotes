#<<---CRUD con listas--->>#
myList = [1, "Hola", False] # Crear
myList2 = ["Hola", 2024, True] # Crear

myList[-1] = True # Reemplazar

myList.append("Python") # Agregar al final de la lista

myList.insert(1, True) # Agregar en una posicion especifica

newList = myList + myList2 # Combinar listas

newList.index("Python") # Buscar datos en la lista

newList.pop() # Eliminar el ultimo valor de la lista

newList.reverse() # Invertir el orden de los valores

newList.remove("Hola") # Eliminar un dato de la lista

numberList = [10,6,3,8,43,68,32]
numberList.sort() # Ordenar numeros de menor a mayor